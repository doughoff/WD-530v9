"use strict";
// function with name
function getOne() {
    return 1;
}
// function assigned to variable
var get1 = function getOne() {
    return 1;
};
// anonymous function assigned to variable
var get2 = function () {
    return 1;
};
// arrow function assigned to variable
var get3 = function () {
    return 1;
};
// arrow function assigned to variable, shortest
var get4 = function () { return 1; };
var get5 = function (x) { return 1; };
var get6 = function (x, y) { return 1; };
