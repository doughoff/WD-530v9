// function with name
function getOne() {
  return 1;
}
// function assigned to variable
let get1 = function getOne() {
  return 1;
};
// anonymous function assigned to variable
let get2 = function() {
  return 1;
};
// arrow function assigned to variable
let get3 = () => {
  return 1;
};
// arrow function assigned to variable, shortest
let get4 = () => 1;
let get5 = x => 1;
let get6 = (x, y) => 1;
